# 例2としてワイブル分布からの乱数生成を考える

import numpy as np
import matplotlib.pyplot as plt
from scipy import stats
from scipy import optimize as opt
from scipy.stats import uniform


def weibull(x, theta):
    """
    ワイブル分布のpdf

    parametors
    ____________
    x: array
        ワイブル分布のpdfを適用するデータ

    theta: int
        ワイブル分布の母数
    ____________
    """

    return 3 / (theta ** 3) * (x ** 2) * np.exp(-((x ** 3) / (theta ** 3)))


def optimize_weibull(theta):
    """
    ワイブル分布の最大値を計算
    """

    res = opt.fmin(lambda x: -weibull(x, theta), 0.1)
    M = weibull(res, theta)
    print("ワイブル分布はx={0}で最大値{1}をとります".format(res, M))

    return M


